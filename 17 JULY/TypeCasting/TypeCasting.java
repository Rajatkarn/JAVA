class Demo 
{ 
     public static void main(String ar[])
	 { 
	    // This is the Example of Implicit TypeCasting 
	     int a=10; double b=a ;
		 System.out.println("This is the Example of Implicit TypeCasting ");
		 System.out.println("a ="+a);
		 System.out.println("b ="+b);
		 
		 // This is the Example of Explicit TypeCasting 
	     double c = 12.5 ; int d;
		 d=(int)c;
		 System.out.println("This is the Example of Explicit TypeCasting");
		 System.out.println("c ="+c);
		 System.out.println("d ="+d);
	 }
}
		 